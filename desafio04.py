n1 = int(input('Digite um numero: '))
n2 = int(input('Digite um numero: '))
s = n1 + n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
rd = n1 % n2
print(f'A soma de {n1} mais {n2} é {s}\nA multiplicação de {n1} por {n2} é {m}\nA divisão de  {n1} por {n2} é {d:.2f}')
print(f'A divisão inteira de {n1} por {n2} é {di}\n {n1} elevado a {n2} é {e}')
print(f'O resto da divisão de {n1} por {n2} é {rd}')

